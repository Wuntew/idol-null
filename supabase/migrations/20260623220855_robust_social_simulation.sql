create policy audience_focus_owner_update on public.audience_focus for update to authenticated using ((select auth.uid()) = user_id) with check ((select auth.uid()) = user_id);
create policy audience_focus_owner_delete on public.audience_focus for delete to authenticated using ((select auth.uid()) = user_id);

